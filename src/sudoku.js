function genPlayerBoard(level) {
  let game = mockeGame;
  let model = emptyGame;
  if (level === 1) {
    model = modE01;
  } else {
    model = modH01;
  }

  for (let lst = 0; lst < 9; lst++) {
    for (let pos = 0; pos < 9; pos++) {
      if (model[lst][pos] === 1) {
        model[lst][pos] = game[lst][pos];
      }
    }
  }
  return model;
}

